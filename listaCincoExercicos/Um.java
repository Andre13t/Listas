package listaCincoExercicos;

/*
 * Faça um programa que peça ao usuário nome e sobrenome separadamente e após junte-os com a mensagem “Seu nome completo é: Nome SobreNome”;
 */

public class Um {
	private String nome = "nome invalido";
	private String sobreNome = " sobre nome invalido";

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getSobreNome() {
		return sobreNome;
	}

	public void setSobreNome(String sobreNome) {
		this.sobreNome = sobreNome;
	}

}
